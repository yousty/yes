    # Aggregate-DSL CommandGroup dummy. Generated by the `command_group`
    # macro in production; here it's hand-rolled (parallel to the
    # hand-rolled regular-command dummies above) so request specs can
    # exercise the new dispatch path without setting up a real
    # Yes::Core::Aggregate subclass. Uses sub-commands that already have
    # an Authorizer defined so BatchAuthorizer doesn't reject them.
    module CommandGroups
      module DoTwoThings
        class Command < Yes::Core::Commands::CommandGroup
          self.context = 'Dummy'
          self.aggregate = 'Activity'
          self.group_name = :do_two_things
          self.sub_command_names = %i[do_something_else do_something_authorized]
        end

        class Authorizer < Yes::Core::Authorization::CommandAuthorizer
          def self.call(_command, _auth_data)
            true
          end
        end

        class Validator < Yes::Core::Commands::Validator
          def self.call(_command)
            true
          end
        end
      end
    end
