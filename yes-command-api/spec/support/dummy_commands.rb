# `Yes::Core::Commands::CommandGroup#sub_command_classes` can resolve them.
# Real aggregate-DSL commands are registered automatically by
# `ClassResolvers::Command::Command#generate_class`; the dummies bypass that
# pipeline, so we do it manually here.
